  public static List<String> watchedVideosByFriends(
      List<List<String>> watchedVideos, int[][] friends, int id, int level) {
    int[] vis = new int[friends.length];
    int cl = 0;
    Queue<Integer> queue = new LinkedList<>();
    queue.add(id);
    vis[id] = 1;
    Map<String, Integer> ans = new HashMap<>();
    while (!queue.isEmpty()) {
      int size = queue.size();
      for (int i = 0; i < size; i++) {
        int cur = queue.poll();
        if (level == cl) {
          for (String video : watchedVideos.get(cur)) {
            ans.put(video, ans.getOrDefault(video, 0) + 1);
          }
        } else {
          for (int child : friends[cur]) {
            if (vis[child] == 0) {
              queue.add(child);
              vis[child] = 1;
            }
          }
        }
      }
      cl++;
    }

    List<String> fin = new ArrayList<>(ans.keySet());

    fin.sort(
        (o1, o2) -> {
          if (ans.get(o1).equals(ans.get(o2))) return o1.compareTo(o2);
          else return ans.get(o1).compareTo(ans.get(o2));
        });
    return fin;
  }

